import java.io.*;
class BinhPhuongVaLapPhuong {
    public static void main(String[] args) {
		System.out.println("Binh Phuong:");
		for(int a=1;a<=10;a++)
		{
			System.out.println(a + " ^2 " + " = " + a*a);
		}
		System.out.println("");
		System.out.println("Lap Phuong:");
		for(int a=1;a<=10;a++)
		{
			System.out.println(a + " ^3 " + " = " +a*a*a);
		}
	}
}